async def seed_if_empty() -> None:
    async with get_session() as session:
        existing = await session.scalar(select(Direction).limit(1))
        if existing is None:
            await _seed_survey(session)
        await _ensure_superadmin(session)
        await session.commit()


async def _seed_survey(session) -> None:
    directions: list[Direction] = []
    for pos, (name, emoji, desc) in enumerate(ROLES):
        d = Direction(name=name, emoji=emoji, description=desc, position=pos)
        session.add(d)
        directions.append(d)
    await session.flush()

    for q_pos, (q_text, answer_texts) in enumerate(QUESTIONS):
        question = Question(text=q_text, position=q_pos, is_active=True)
        session.add(question)
        await session.flush()
        for a_pos, a_text in enumerate(answer_texts):
            answer = Answer(question_id=question.id, text=a_text, position=a_pos)
            session.add(answer)
            await session.flush()
            # i-й вариант → i-я роль, +1 балл
            session.add(
                AnswerWeight(
                    answer_id=answer.id,
                    direction_id=directions[a_pos].id,
                    points=1,
                )
            )


async def _ensure_superadmin(session) -> None:
    if SUPERADMIN_ID <= 0:
        return
    exists = await session.get(SuperAdmin, SUPERADMIN_ID)
    if exists is None:
        session.add(SuperAdmin(telegram_id=SUPERADMIN_ID, added_by=None))
